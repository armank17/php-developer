     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $email = $request->input('email');
        if(isset($email)){
            $users = User::where('email', 'like', '%'.$email.'%')->paginate(10);
        }else{
            $users = User::paginate(10);
        }
        
        
        if($request->ajax()){
           return $users;
        }else{
            return view('user');
        }
        
        
    }

